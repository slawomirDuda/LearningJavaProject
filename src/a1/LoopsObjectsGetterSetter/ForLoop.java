package a1.LoopsObjectsGetterSetter;

public class ForLoop {
    public static void method() {
        String[] cars = {"Volvo", "BMW", "Ford", "Mazda"};
        int i;
        for (i = 0; i < cars.length; i++) {
            System.out.println(cars[i]);
        }
        System.out.println(i);

    }
}
